"""禁止表現チェッカー — 理の声に「断定・煽り・脅し」が混入していないか守る。

カードを表示・投稿する前に必ず通す。1件でも引っかかったら出さない。
これは理カードの“誠実さ＝堀”を機械的に担保する仕組み。"""

# カテゴリごとの禁止語。理の声は「淡々・非断定」なので、本来ここに引っかからない。
BANNED = {
    "断定": ["必ず", "絶対", "確実に", "間違いなく", "100%", "保証します"],
    "煽り・不安": ["危険", "警告", "不幸", "呪い", "祟り", "たたり", "災い",
                  "最悪", "地獄", "破滅", "終わりです"],
    "金運の断定": ["大儲け", "必ず儲か", "儲かります", "金運最高", "億万長者",
                  "大金が入ります"],
    "緊急・強制": ["今すぐ", "急いで", "買わないと", "しないと損", "今だけ", "手遅れ"],
    "医療の断定": ["必ず治", "治ります", "完治します", "病気が治る"],
}


def check_text(text):
    """禁止語に当たった (カテゴリ, 語) のリストを返す。空ならクリーン。"""
    hits = []
    for category, words in BANNED.items():
        for w in words:
            if w in text:
                hits.append((category, w))
    return hits


def is_clean(text):
    return len(check_text(text)) == 0
