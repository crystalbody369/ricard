def _pick(variants, seed):
    return variants[seed % len(variants)]


def compose(m):
    """build_flow の素材 → 整った『今日の理』カード。"""
    g, fav, brel, seed = m["group"], m["favor"], m["branch_rel"], m["seed"]
    prefix = "急がず、" if brel == "clash" else ""
    ugoki = "%sもし動くなら、%sが落ち着きます。" % (prefix, m["timing"])

    return {
        "date": m["date"],
        "honmei_star": m["honmei_star"],
        "ten_god": m["ten_god"],
        "opening": _pick(OPENING[g][fav], seed),
        "flow": _pick(FLOW[g][fav], seed + 1),
        "en_hito": _pick(EN_HITO[brel], seed + 2),
        "ugoki": ugoki,
        "totonoe": _pick(TOTONOE[g][fav], seed + 3),
        "closing": _pick(CLOSING[g], seed + 4),
        "footer": m["disclaimer"],
    }


def render_text(card):
    """投稿・プレビュー用のプレーンテキスト。"""
    lines = [
        "｜今日の理｜　%s" % card["date"],
        "",
        card["opening"],
        "",
        "◦ 流れ　… %s" % card["flow"],
        "◦ 縁・人 … %s" % card["en_hito"],
        "◦ 動き　… %s" % card["ugoki"],
        "◦ 整える … %s" % card["totonoe"],
        "",
        card["closing"],
        "",
        "（%s）" % card["footer"],
    ]
    return "\n".join(lines)


def build_card(birth, date):
    """生年月日・対象日 → 一人ひとり別の『今日の理』カード。"""
    return compose(build_flow(birth, date))
